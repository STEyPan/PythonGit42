firstNumber = int(input("Введите первое число из диапазона: "))
lastNumber = int(input("Введите последнее число из диапазона: "))

for num1 in range(firstNumber, lastNumber+1):
    for num2 in range(1, 11):
        print(f"{num1:2}  * {num2:2} = {num1*num2:2}", end="   ")
    print("\n ----------------------------------------------------------------------------", end="---")
    print("-------------------------------------------------------------------------------")